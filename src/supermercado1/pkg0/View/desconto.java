    private void valorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_valorKeyReleased
        float comdesc=0,tax;
        total=subtotal+taxa;
        try {
            this.desc=Float.valueOf(valor.getText());
            if(desc>=subtotal)
            {
                new Message().messageError("O desconto aplicado Ã© invÃ¡lido!");
                valor.setText("");
                percetagem.setText("");
                cptotal.setText((subtotal+taxa)+"");
                cptaxa.setText(taxa+"");
                aux4.setText(taxa+"");
                aux.setText(""+total);
                cptotaldesc.setText("");
                aux3.setText("");
            }else{
                comdesc=subtotal-desc;  
                tax=comdesc*Taxa/100;
                cptaxa.setText(tax+"");
                cptotaldesc.setText(comdesc+"");
                total=comdesc+tax;
                perce=(desc*100)/subtotal;
                aux4.setText(tax+"");
                percetagem.setText(perce+"%");
                cptotal.setText(""+total);
                aux.setText(""+total);
                aux3.setText(""+desc);
            }
        } catch (Exception e) {
            cptotal.setText(""+(subtotal+taxa));
            aux.setText(""+total);
            cptotaldesc.setText("");
            cptaxa.setText(taxa+"");
            aux4.setText(taxa+"");
            percetagem.setText("");aux3.setText("");
        }
    }//GEN-LAST:event_valorKeyReleased

    private void percetagemKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_percetagemKeyReleased
        float comdesc,tax;
        total=subtotal+taxa;
        try {
            this.perce=Float.valueOf(percetagem.getText());
            if(perce>100)
            {
                new Message().messageError("O desconto aplicado Ã© invÃ¡lido!");
                percetagem.setText("");
                valor.setText("");
                cptotal.setText(""+total);
                aux.setText(""+total);
                cptotaldesc.setText("");aux3.setText("");aux4.setText(taxa+"");
            }else{
                desc=subtotal*perce/100;
                comdesc=subtotal-desc;  
                tax=comdesc*Taxa/100;
                cptaxa.setText(tax+"");
                cptotaldesc.setText(comdesc+"");
                total=comdesc+tax;
                valor.setText(desc+"");
                cptotal.setText(""+total);
                aux.setText(""+total);
                cptotaldesc.setText((subtotal-desc)+"");aux3.setText(""+desc);
                aux4.setText(tax+"");
            }
        } catch (Exception e) {
            cptotal.setText(""+total);
            aux.setText(""+total);cptotaldesc.setText("");
            valor.setText("");aux3.setText("");
            aux4.setText(taxa+"");
        }
    }//GEN-LAST:event_percetagemKeyReleased

