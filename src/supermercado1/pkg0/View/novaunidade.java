    public void novaUnidade()
    {
        if(nome.getText().trim().isEmpty())
        {
            new Message().messageError("Preencha o campo em branco!");
        }else
        {
            unidadeBean cb=new unidadeBean();
            cb.setNome(nome.getText());
            unidadeDAO cd=new unidadeDAO();
            if(cd.novaUnidade(cb))
            {
                new Message().messageSucesso("Unidade cadastrado com sucesso!!");
            }else
            {
                new Message().messageError("Verifique se a unidade não está cadastrado!");
            }
        }
    }
    
    public void updateUnidade()
    {
        if(nome.getText().trim().isEmpty())
        {
            new Message().messageError("Preencha o campo em branco!");
        }else
        {
            ub.setNome(nome.getText());
            
            if(new unidadeDAO().uptadeUniddae(ub))
            {
                new Message().messageSucesso("Unidade actualizado com sucesso!!");
                new unidadeDAO().getUnidade(table);
                dispose();
            }else
            {
                new Message().messageError("Erro ao actualizar os dados!");
            }
        }
    }

